def walk(maze_map, i, j):
    if i == 10 and j == 10:
        #mark as arrived
        return "a"
    #S
    if maze_map[i+1][j] == 0:
        maze_map[i+1][j] = -1
        temp = walk(maze_map, i+1, j)
        #if passable
        if temp:
            return "S" + temp
    #N
    if maze_map[i-1][j] == 0:
        maze_map[i-1][j] = -1
        temp = walk(maze_map, i-1, j)
        #if passable
        if temp:
            return "N"+temp
    #E
    if maze_map[i][j+1] == 0:
        maze_map[i][j+1] = -1
        temp = walk(maze_map, i, j+1)
        #if passable
        if temp:
            return "E"+temp
    #W
    if maze_map[i][j-1] == 0:
        maze_map[i][j-1] = -1
        temp = walk(maze_map, i, j-1)
        #if passable
        if temp:
            return "W"+temp

def checkio(maze_map):
    return walk(maze_map,1,1)[:-1]

